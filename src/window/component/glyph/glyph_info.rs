pub struct GlyphInfo {
    // Координаты в атласе (в пикселях или 0..1)
    pub uv_min: [f32; 2],
    pub uv_max: [f32; 2],
    // Размеры самого символа в пикселях
    pub width: f32,
    pub height: f32,
    // Смещения (например, для буквы 'g', которая уходит ниже базовой линии)
    pub x_offset: f32,
    pub y_offset: f32,
    // Сколько места символ занимает в строке (горизонтальный шаг)
    pub advance: f32,
}

